#include <stdio.h>// inclusion de la libreria Standar Input Output
//prototipo de función
void ft_print_alphabet (void);
/**
 * función que muestra el alfabeto en minúsculas, en
 * una sola línea, en orden ascendente, comenzando por la letra 'a'…'z'
 * 
 * **/
void ft_print_alphabet ()
{
    char letra = 97;// letra donde inicia el alfabeto
    while (letra<123)// ciclo que recorre el alfabeto
    {
        printf("%c",letra);
        printf(" ");
        letra += 1;
    }    
}
/**
 * Declaración de la función main.
 * 
 * **/
int main ()
{
    ft_print_alphabet();
    return 0;
}
